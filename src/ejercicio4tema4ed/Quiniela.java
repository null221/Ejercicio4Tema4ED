    private void bRealizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bRealizarActionPerformed
        Random aleatorio= new Random();
        DefaultListModel model=new DefaultListModel();
        DefaultListModel model1= (DefaultListModel) ListPartido.getModel();
        String resultado="";
        
        for (int i = 0; i < model1.size(); i++) 
        {
          int numero =aleatorio.nextInt(3-1)+1;
            
            if(numero==1)
            {
                resultado = "X";
            }
            
            else if(numero==2)
            {
                resultado = "1";
            }
            
            else if(numero==3)
            {
                resultado = "2";
            }
         String partid=model1.get(i).toString()+"--> "+resultado;
         model.addElement(partid);
            
        }
          ListResultado.setModel(model);
          ListPartido.setModel(new DefaultListModel());
        
    }//GEN-LAST:event_bRealizarActionPerformed

