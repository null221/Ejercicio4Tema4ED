    /**
     * metodo para agregar el partido a la lista de partidos
     * @param partido que enfrenta a los dos equipos introducidos
     */
    public void agregarPartido(String partido)
    {
        model= new DefaultListModel();
        for (int i = 0; i < ListPartido.getModel().getSize(); i++) 
        {
            model.addElement(ListPartido.getModel().getElementAt(i));      
        }
        
        model.addElement(partido);
        ListPartido.setModel(model);
    }
    
    /**
     * metodo para eliminar un partido
     * @param numero en la posicion de la lista del partido a borrar
     */
    public void borrar(int numero)
    {
        ListPartido.remove(numero);
    }
    
    

